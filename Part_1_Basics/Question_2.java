package Part_1_Basics;

public class Question_2 {
    public static void main(String[] args) {
//        Datatype in Java

//        Integer
        int a = 40;

//        Whole number
        double b= 4.5;
        float c = 4.5f;

//        Single character
        char d = 'd';

//        Sequence of character
        String str = "Rahul";

//        True or false
        boolean bool = true;
        bool = false;


//        Operators in Java

//        Addition
        int num1 = 50;
        int num2 = 4;
        int sum = num1 + num2;
        System.out.println(sum);

//        Subtraction
        int num3 = 53;
        int num4 = 22;
        int sub = num3 - num4;
        System.out.println(sub);

//        Product
        int num5 = 44;
        int num6 = 2;
        int prd = num5 * num6;
        System.out.println(prd);

//        Division (quotient)
        int div = num1 / num2;
        System.out.println(div);

//        Division (Remainder)
        int rem = num1 % num2;
        System.out.println(rem);
    }
}
